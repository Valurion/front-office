            <h2>L’offre de services de Cairn.info</h2>

            <p>Cairn.info a pour objectif de faciliter la mise en ligne des publications de sciences humaines et sociales en aidant les maisons d’édition et autres structures éditoriales, à gérer cette période complexe où doivent coexister une version électronique et une version papier des mêmes publications. Pour tenir compte de la diversité des situations, notre offre est modulaire, quasiment « à la carte » :</p>

            <ul class="bullets_list">
                <li >
                    <span class="icon icon-diamond-bullet"></span>Le cœur de l’offre est constitué d’un service d’hébergement et de distribution des publications de sciences humaines et sociales, sous forme numérique, via le portail www.cairn.info.</li>
                <li>
                    <span class="icon icon-diamond-bullet"></span>Mais ce service peut être complété par d’autres prestations, en fonction des besoins spécifiques de chaque publication.</li>
                <li>
                    <span class="icon icon-diamond-bullet"></span>Conversion ou rétroconversion – c’est-à-dire conversion rétrospective – des fichiers prépresse de façon à produire les fichiers XML, structurés selon la DTD EruditArticle, nécessaires à la présentation des articles sur le portail www.cairn.info.</li>
                <li>
                    <span class="icon icon-diamond-bullet"></span>Pour les revues, composition et mise en page, de façon à produire directement des fichiers utilisables à la fois pour la fabrication papier et électronique des revues.</li>
                <li>
                    <span class="icon icon-diamond-bullet"></span>Pour les revues, filtrage (par mot de passe) des accès éventuellement accordés par l’éditeur aux abonnés à la version papier du périodique.</li>
                <li>
                    <span class="icon icon-diamond-bullet"></span>Gestion des abonnements aux versions papier et numérique.</li>
                <li>
                    <span class="icon icon-diamond-bullet"></span>Diffusion enfin de ces publications sous forme numérique grâce notamment à un effort commercial ad hoc vis-à-vis des bibliothèques (insertion des publications dans des bouquets de publications et commercialisation de ces bouquets auprès des institutions et établissements de prêt).</li>
            </ul>

            <p>Ces différents services sont proposés non seulement aux publications des quatre maisons d’édition à l’initiative du projet mais également à tous les acteurs, quel qu’en soit le statut, ayant en charge l’édition et/ou la diffusion de publications de sciences humaines et/ou sociales de qualité.</p>

