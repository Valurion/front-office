        <h2>Qui sommes-nous ?</h2>
        <p>
            Cairn.info est né de la volonté de quatre maisons d'édition (<a href="http://www.editions-belin.com/" target="_blank" class="link-underline">Belin</a>, <a href="http://www.deboeck.com/" target="_blank" class="link-underline">De Boeck</a>, <a href="http://www.editionsladecouverte.fr/" target="_blank" class="link-underline">La Découverte</a> et <a href="http://www.editions-eres.com/" target="_blank" class="link-underline">Erès</a>) ayant en charge la publication et la diffusion de revues de sciences humaines et sociales, d’unir leurs efforts pour améliorer leur présence sur l’Internet, et de proposer à d’autres acteurs souhaitant développer une version électronique de leurs publications, les outils techniques et commerciaux développés à cet effet.<br />

            En février 2006, la <a href="http://www.bnf.fr/" target="_blank" class="link-underline">Bibliothèque nationale de France</a> s’est associée à ce projet, de façon à faciliter le développement d’une offre éditoriale francophone, sous forme numérique.<br />

