        <h2>Nos activités</h2>
        <p>
            L’ambition de Cairn.info est d’aider les maisons d’édition, organismes ou associations ayant en charge des publications de sciences humaines francophones à gérer la coexistence des formats "papier" et électronique. Dans ce but, les services de Cairn.info couvrent à la fois la fabrication papier et électronique, la distribution papier (gestion des abonnements pour les revues, routage) et électronique (texte intégral en ligne, distribution des métadonnées auprès des sites et bases bibliographiques), ainsi que la diffusion et la promotion de ces publications auprès des publics auxquels elles s’adressent.<br />

            Cairn.info n'est donc nullement une maison d'édition. Les auteurs d'articles sont ainsi invités à soumettre leurs propositions directement aux revues de leur choix ou à leurs éditeurs (les références de ceux-ci se trouvant habituellement en cliquant sur le bouton "En savoir plus" présent sur la page de présentation de chaque revue).<br />

            Nous insistons notamment sur le fait que notre plate-forme est totalement indépendante et économiquement distincte de la maison d'édition <a href="http://www.editions-cairn.fr/" target="_blank" class="link-underline">"Cairn Editions"</a>.
        </p>

